/*
перебрать числа от 1 до 100
если число делится на 3 - вывести вместо него Fizz
если на 5 - вывести вместо него Buzz
если и на 3 и на 5 - вывести вместо него FizzBuzz
*/

for (let i = 0; i < 100; i++) {
   let msg = '';
   if (i % 3 === 0) msg += "Fizz"
   if (i % 5 === 0) msg += "Buzz"
   if (msg) console.log(msg);
}